import numpy as np

arr = [4,1,0,-2,5,7,-3,0,0,5,0,-1,3,6,9,5,7,11,9,6,0]
arr = np.array(arr)
print("Найтепліше було в %d день"%(np.argmax(arr)+1))
print("Чи перевищувала температура 10 градусів? " + ("Так" if np.any(arr > 10) else "Ні"))
print("Чи перевищувала температура 20 градусів? " + ("Так" if np.any(arr > 20) else "Ні"))
print("Чи всі дні температура була нижче 10 градусів? " + ("Так" if np.all(arr < 10) else "Ні"))
print("Посортовані значення " + str(np.sort(arr)))
